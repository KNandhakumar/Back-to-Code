package com.Nandhakumar.Java_Basics.Day_3;

public class Loops {
    public static void main(String[] args) {
        // For loop : In java, a loop used to repeatedly execute a block of
        // statements for a specific number of times.
        for (int i = 0; i <=4 ; i++) {
            System.out.println("Hi");
        }
    }
}
