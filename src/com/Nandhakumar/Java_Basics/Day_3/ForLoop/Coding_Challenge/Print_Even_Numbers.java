package com.Nandhakumar.Java_Basics.Day_3.ForLoop.Coding_Challenge;

public class Print_Even_Numbers {
    public static void main(String[] args) {
        // Question : Print event numbers between 1 to 10 using for loop and if else
        for (int i = 1; i <=10 ; i++) {
            if (i%2==0){
                System.out.println(i);
            }
        }
    }
}
