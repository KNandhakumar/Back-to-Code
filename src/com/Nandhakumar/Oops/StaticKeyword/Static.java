package com.Nandhakumar.Oops.StaticKeyword;

public class Static {
    public static void main(String[] args) {
        // In java, static keyword means a variable, method or block belongs to the class rather than
        // any specific instance(object)
        // static variable -> shared across all objects of the class.
        // static method -> can be called without creating of the class.
        // static block -> runs once when the class is loaded.
        // static class (nested) -> a class inside another class that dosent need an instance of the outer class.
        // usage -> saves memory, useful for utility methods.
    }
}
